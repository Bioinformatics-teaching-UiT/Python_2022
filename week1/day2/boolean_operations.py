"""
Playing around with Booleans
Python is flexible in assignment of truth values to things
Essentially the things that are False in Python are:
    None, empty string, 0, False
Everything else maps to True
This is useful in your if statements!!
"""

# what is the type for a boolean called?
print(type(True))

# play around with bool() casting
print(bool(0))
print(bool(1))
print(bool('a'))
print(bool(10))
print(bool(''))
# None
print(bool(None))
