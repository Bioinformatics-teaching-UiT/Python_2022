"""
This script contains short number operations.
Uses +,-, *, /, //, %, **
"""
print(1+1)
print(10-101)
print(2*-3-1)

# regular division produces a float
print(7/3)
# integer division gives you the closest integer smaller than your result
print(7//3)
# modulus operator gives you the remainder
print(7%3)
print(2**2)

num = -3//2
print(num)