"""
This is a pizza script!
Calculates volume of a pizza
Takes in user input for the three variables
"""

pi = float(input('Please enter a value for pi'))
z = int(input('Please enter a value for your pizza radius'))
a = input('Please enter your pizza height')
b = int(a)
print('Pizza radius', z)
print('Pizza height', a)
print('Value for pi', pi)

V = pi * z * z * b
print('Volume of pizza', V)



